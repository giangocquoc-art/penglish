type Seed = {
  id: string;
  unitId: string;
  level: GeneratedReadingCefrLevel;
  titleVi: string;
  titleEn: string;
  passage: string;
  setup: string;
  pattern: string;
  patternVi: string;
  focusSentence: string;
  focusMeaningVi: string;
  q1: [string, string, string[], string, string];
  q2: [string, string, string[], string, string];
  blank: [string, string, string, string];
  order: [string, string[], string];
  vocab: Array<[string, string, string, string, string]>;
};

function lesson(seed: Seed): GeneratedReadingLessonSource {
  return {
    id: seed.id,
    unitId: seed.unitId,
    level: seed.level,
    titleVi: seed.titleVi,
    titleEn: seed.titleEn,
    passage: seed.passage,
    vietnameseSetup: seed.setup,
    sentenceFocus: {
      pattern: seed.pattern,
      explanationVi: seed.patternVi,
      examples: [{ text: seed.focusSentence, meaningVi: seed.focusMeaningVi }],
    },
    comprehensionQuestions: [
      {
        id: `${seed.id}-q1`,
        questionVi: seed.q1[0],
        questionEn: seed.q1[1],
        options: seed.q1[2],
        answer: seed.q1[3],
        explanationVi: seed.q1[4],
      },
      {
        id: `${seed.id}-q2`,
        questionVi: seed.q2[0],
        questionEn: seed.q2[1],
        options: seed.q2[2],
        answer: seed.q2[3],
        explanationVi: seed.q2[4],
      },
    ],
    fillBlankTasks: [
      {
        id: `${seed.id}-blank`,
        prompt: seed.blank[0],
        answer: seed.blank[1],
        hintVi: seed.blank[2],
        explanationVi: seed.blank[3],
      },
    ],
    sentenceOrderingTasks: [
      {
        id: `${seed.id}-order`,
        vietnamese: seed.order[0],
        words: seed.order[1],
        answer: seed.order[2],
        explanationVi: 'Sắp xếp theo trật tự câu tiếng Anh tự nhiên: chủ ngữ trước, động từ sau, rồi thông tin bổ sung.',
      },
    ],
    vocabularyFocus: seed.vocab.map(([term, meaningVi, partOfSpeechOrType, example, exampleMeaningVi], index) => ({
      id: `${seed.id}-vocab-${index + 1}`,
      term,
      meaningVi,
      partOfSpeechOrType,
      example,
      exampleMeaningVi,
    })),
    whaleCoachLines: [
      'Đọc ý chính trước, đừng dịch từng chữ.',
      'Gạch thầm từ khóa trong câu hỏi rồi quay lại đoạn đọc.',
      'Enter để kiểm tra câu điền từ; A/B/C/D hoặc 1/2/3/4 để chọn đáp án nhanh.',
    ],
    source: cefrSpSource,
  };
}

